/* ************************************************************************** */
/*  -2147483648                                                               */
/*  +2147483647                                                               */
/* ************************************************************************** */
int	arg_range_int(char *arg, int *my_int)
{	
	long	arg_num;

	arg_num = ft_atol(arg);
	if (INT_MIN <= arg_num && arg_num <= INT_MAX)
	{	*my_int = (int)arg_num;
		return (1);
	}else
		return (0);
}
