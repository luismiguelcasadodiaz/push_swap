/* ************************************************************************** */
/* ps_sor4() sort a 4-element stack                                           */
/*                                                                            */
/* 1.- Pass 1 elements to stack b                                             */
/* 2.- Sort 3 elemnets of stack a                                             */
/* 3.- from stack b add the 4th element to stack a                            */
/*                                                                            */
/* ************************************************************************** */
void	ps_sor4(t_pss *a, t_pss *b)
{
	pss_psoo(b, a, PRINTME);
	ps_sor3(a);
	ps_add4(a, b);
}
