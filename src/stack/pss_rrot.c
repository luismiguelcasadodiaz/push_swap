#include "libpss.h"

/* ************************************************************************** */
/* pss_rrot() moves top to the top's next node, making the last  the first.   */
/*                                                                            */
/* ************************************************************************** */
void	pss_rrot(t_pss *s, int printme)
{
	s->top = s->top->prev;
	if (printme)
		ft_printf("rr%c\n", s->nam);
}
