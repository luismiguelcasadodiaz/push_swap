#include "libpss.h"

/* ************************************************************************** */
/* pss_rota() moves top to the top's next node, making the first the last.    */
/*                                                                            */
/* ************************************************************************** */
void	pss_rota(t_pss *s, int printme)
{
	s->top = s->top->next;
	if (printme)
		ft_printf("r%c\n", s->nam);
}
